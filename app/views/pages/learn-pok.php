    <section class="basic-rules">
      <h2>Normas Básicas</h2>
      <p class="learn-p">Pokémon TCG es un juego donde dos jugadores se enfrentan usando un mazo de 60 cartas. El
        objetivo es reducir
        los puntos de vida del oponente a cero, usando Pokémon, entrenadores y cartas de energía.</p>
      <ul class="learn-ul">
        <li><strong>Fase de Robar:</strong> Los jugadores comienzan el turno robando una carta de su mazo.</li>
        <li><strong>Fase de Colocar:</strong> Los jugadores pueden colocar Pokémon en el banco o en el campo de batalla.
        </li>
        <li><strong>Fase de Ataque:</strong> El jugador puede atacar con uno de sus Pokémon en el campo.</li>
        <li><strong>Fase Final:</strong> El jugador termina su turno y el oponente comienza el suyo.</li>
      </ul>
    </section>

    <!-- Jugadores -->
    <section class="players">
      <h2>Jugadores</h2>
      <p class="learn-p">Pokémon TCG se juega entre dos jugadores, cada uno con un mazo de 60 cartas. El objetivo es
        reducir los puntos
        de vida del oponente a cero. Los jugadores usan Pokémon para atacar y defender, entrenadores para ayudar a sus
        Pokémon, y cartas de energía para poder usar los ataques.</p>
    </section>

    <!-- Requisitos -->
    <section class="requirements">
      <h2>Requisitos para Jugar</h2>
      <ul class="learn-ul">
        <li><strong>Cartas:</strong> Cada jugador necesita un mazo de 60 cartas.</li>
        <li><strong>Puntos de vida:</strong> Los jugadores comienzan con 60 puntos de vida.</li>
        <li><strong>Un campo de juego:</strong> Un espacio donde colocar las cartas, generalmente con zonas para Pokémon
          en el banco y en el campo de batalla, así como para cartas de energía y entrenadores.</li>
      </ul>
    </section>

    <!-- Formatos de Juego -->
    <section class="formats">
      <h2>Formatos de Juego</h2>
      <p class="learn-p">Pokémon TCG tiene varios formatos de juego, cada uno con sus propias reglas y restricciones. A
        continuación, te
        explicamos los tres formatos más populares:</p>

      <h3>Estándar</h3>
      <p class="learn-p">El formato Estándar está compuesto por las cartas de las últimas expansiones lanzadas. Los
        jugadores deben
        construir sus mazos utilizando solo cartas de las últimas colecciones permitidas por el formato Estándar.</p>
      <ul class="learn-ul">
        <li><strong>Restricciones:</strong> Solo se pueden usar cartas de las últimas expansiones permitidas.</li>
        <li><strong>Duración:</strong> Este formato cambia constantemente con el lanzamiento de nuevas expansiones.</li>
      </ul>

      <h3>Expandido</h3>
      <p class="learn-p">El formato Expandido incluye cartas de muchas más expansiones que el formato Estándar. Los
        jugadores pueden
        usar cartas de colecciones más antiguas, lo que permite una mayor variedad de estrategias.</p>
      <ul class="learn-ul">
        <li><strong>Restricciones:</strong> Se permiten cartas de expansiones más antiguas, pero algunas cartas están
          prohibidas por su poder desequilibrante.</li>
        <li><strong>Duración:</strong> El formato se mantiene estable, ya que las cartas de las colecciones permitidas
          son mucho más amplias.</li>
      </ul>

      <h3>Sin Límites</h3>
      <p class="learn-p">En el formato Sin Límites, los jugadores pueden usar cualquier carta que haya sido publicada,
        sin restricciones
        en cuanto a la antigüedad de las cartas. Este formato permite mazos muy variados, pero también puede ser muy
        desequilibrado debido a la presencia de cartas poderosas de colecciones antiguas.</p>
      <ul class="learn-ul">
        <li><strong>Restricciones:</strong> No hay restricciones de cartas; cualquier carta publicada es legal.</li>
        <li><strong>Duración:</strong> Este formato no cambia, ya que permite todas las cartas disponibles.</li>
      </ul>
    </section>

