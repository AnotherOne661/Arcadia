    <!-- ¿Qué son las cookies? -->
    <section class="what-are-cookies">
      <h2>¿Qué son las Cookies?</h2>
      <p>Las cookies son pequeños archivos de texto que se almacenan en tu dispositivo (ordenador, smartphone, tablet)
        cuando visitas un sitio web. Permiten al sitio web recordar tus preferencias y acciones para mejorar tu
        experiencia.</p>
    </section>

    <!-- Tipos de cookies que utilizamos -->
    <section class="types-of-cookies">
      <h2>Tipos de Cookies que Utilizamos</h2>
      <p>En nuestro sitio web utilizamos los siguientes tipos de cookies:</p>
      <ul>
        <li><strong>Cookies esenciales:</strong> Necesarias para el funcionamiento básico del sitio web. Sin ellas, no
          podrías navegar ni utilizar ciertas funciones.</li>
        <li><strong>Cookies de rendimiento:</strong> Nos ayudan a entender cómo interactúan los usuarios con nuestro
          sitio, recopilando información de forma anónima.</li>
        <li><strong>Cookies funcionales:</strong> Permiten recordar tus preferencias, como el idioma o región, para
          ofrecerte una experiencia personalizada.</li>
        <li><strong>Cookies de publicidad:</strong> Utilizadas para mostrar anuncios relevantes según tus intereses.
          También limitan la cantidad de veces que ves un anuncio.</li>
        <li><strong>Cookies de terceros:</strong> Cookies establecidas por servicios externos que utilizamos, como
          Google Analytics o redes sociales.</li>
      </ul>
    </section>

