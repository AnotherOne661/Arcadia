    <section class="basic-rules">
      <h2>Normas Básicas</h2>
      <p>En One Piece TCG, dos jugadores se enfrentan usando un mazo de cartas que representa su tripulación de piratas.
        El objetivo es reducir los puntos de vida del oponente a cero o cumplir ciertos objetivos estratégicos para
        ganar la partida.</p>
      <ul>
        <li><strong>Fase de Preparación:</strong> Ambos jugadores preparan su mazo y colocan cartas en sus respectivas
          zonas de juego.</li>
        <li><strong>Fase de Juego:</strong> Los jugadores juegan cartas de su mano para invocar personajes, lanzar
          ataques y usar habilidades especiales.</li>
        <li><strong>Fase de Combate:</strong> Los personajes luchan entre sí y contra el oponente, tratando de reducir
          los puntos de vida.</li>
        <li><strong>Fase Final:</strong> El turno de un jugador termina y el siguiente jugador comienza su fase.</li>
      </ul>
    </section>

    <!-- Jugadores -->
    <section class="players">
      <h2>Jugadores</h2>
      <p>El juego se juega entre dos jugadores, cada uno con un mazo de cartas de 50 a 60 cartas. Los jugadores
        controlan personajes de la serie y utilizan habilidades para luchar y completar objetivos durante el juego.</p>
    </section>

    <!-- Requisitos -->
    <section class="requirements">
      <h2>Requisitos para Jugar</h2>
      <ul>
        <li><strong>Cartas:</strong> Cada jugador debe tener un mazo de entre 50 y 60 cartas. Las cartas incluyen
          personajes, eventos y habilidades especiales.</li>
        <li><strong>Puntos de vida:</strong> Los jugadores comienzan con 10 puntos de vida.</li>
        <li><strong>Un campo de juego:</strong> Un espacio donde colocar las cartas, con zonas específicas para
          personajes, eventos y recursos.</li>
      </ul>
    </section>

    <!-- Formatos de Juego -->
    <section class="formats">
      <h2>Formatos de Juego</h2>
      <p>One Piece TCG tiene varios formatos que ofrecen diferentes estilos de juego. A continuación, te explicamos dos
        de los formatos más populares:</p>

      <h3>Standard</h3>
      <p>En el formato Standard, los jugadores deben construir su mazo usando cartas de las últimas colecciones
        permitidas por el formato. Este formato se juega en torneos oficiales y es el más común para la mayoría de los
        jugadores.</p>
      <ul>
        <li><strong>Restricciones:</strong> Solo se pueden usar cartas de las colecciones más recientes que sean legales
          en el formato Standard.</li>
        <li><strong>Duración:</strong> Este formato es ideal para jugadores que desean competir en eventos oficiales y
          torneos.</li>
      </ul>

      <h3>Draft</h3>
      <p>En el formato Draft, los jugadores abren paquetes de cartas y seleccionan cartas de manera secuencial para
        construir un mazo durante el evento. Este formato es ideal para eventos de lanzamiento y es perfecto para
        quienes disfrutan de la sorpresa de crear un mazo a partir de cartas limitadas.</p>
      <ul>
        <li><strong>Restricciones:</strong> Los jugadores deben construir su mazo seleccionando cartas de los paquetes
          de cartas proporcionados durante el evento.</li>
        <li><strong>Duración:</strong> El formato Draft es perfecto para eventos rápidos y para aquellos que buscan una
          experiencia más dinámica.</li>
      </ul>
    </section>

