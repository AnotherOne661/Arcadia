    <!-- Normas Básicas -->
    <section class="basic-rules">
      <h2>Normas Básicas</h2>
      <p>En Lorcana TCG, dos jugadores se enfrentan utilizando un mazo de cartas, donde el objetivo es reducir los
        puntos de vida del oponente a cero o completar misiones. Los jugadores usan cartas de personajes, hechizos y
        habilidades para controlar el juego.</p>
      <ul>
        <li><strong>Fase de Preparación:</strong> Los jugadores preparan sus mazos y colocan cartas en su zona de juego.
        </li>
        <li><strong>Fase de Juego:</strong> Los jugadores juegan cartas de su mano, colocan personajes en el campo y
          usan habilidades especiales.</li>
        <li><strong>Fase de Ataque:</strong> Los jugadores pueden atacar a su oponente con personajes en el campo.</li>
        <li><strong>Fase Final:</strong> El jugador termina su turno y el oponente comienza el suyo.</li>
      </ul>
    </section>

    <!-- Jugadores -->
    <section class="players">
      <h2>Jugadores</h2>
      <p>El juego se juega entre dos jugadores. Cada jugador debe tener un mazo de cartas con un mínimo de 60 cartas,
        que incluye personajes, hechizos y cartas de habilidades. Los jugadores usan estas cartas para atacar, defender
        y completar misiones durante el juego.</p>
    </section>

    <!-- Requisitos -->
    <section class="requirements">
      <h2>Requisitos para Jugar</h2>
      <ul>
        <li><strong>Cartas:</strong> Cada jugador necesita un mazo de al menos 60 cartas, que debe incluir personajes,
          hechizos y habilidades.</li>
        <li><strong>Puntos de vida:</strong> Los jugadores comienzan con 20 puntos de vida.</li>
        <li><strong>Un campo de juego:</strong> Un espacio donde colocar las cartas, con zonas para los personajes,
          hechizos y habilidades.</li>
      </ul>
    </section>

    <!-- Formatos de Juego -->
    <section class="formats">
      <h2>Formatos de Juego</h2>
      <p>Lorcana TCG tiene varios formatos de juego que ofrecen experiencias diferentes. A continuación, te explicamos
        los tres formatos más populares:</p>

      <h3>Core Constructed</h3>
      <p>En el formato Core Constructed, los jugadores deben construir un mazo utilizando cartas de las colecciones
        principales de Lorcana. Este formato permite a los jugadores usar solo las cartas de las colecciones que están
        en el formato estándar de Lorcana.</p>
      <ul>
        <li><strong>Restricciones:</strong> Solo se pueden usar cartas de las colecciones principales y permitidas por
          el formato Core Constructed.</li>
        <li><strong>Duración:</strong> Este formato es ideal para jugadores que prefieren jugar con cartas estables y
          conocidas.</li>
      </ul>

      <h3>Booster</h3>
      <p>En el formato Booster, los jugadores abren paquetes de cartas y construyen un mazo en función de las cartas que
        han obtenido. Este formato es ideal para eventos de lanzamiento y para quienes disfrutan de la sorpresa de
        construir un mazo con cartas limitadas.</p>
      <ul>
        <li><strong>Restricciones:</strong> Los jugadores construyen su mazo utilizando solo las cartas que obtienen de
          los paquetes de cartas.</li>
        <li><strong>Duración:</strong> Este formato es perfecto para eventos de construcción rápida y eventos sociales.
        </li>
      </ul>

      <h3>Draft</h3>
      <p>En el formato Draft, los jugadores seleccionan cartas de un conjunto limitado de cartas (por lo general, cartas
        de un booster pack) y construyen un mazo en el transcurso del evento. Los jugadores seleccionan cartas de manera
        secuencial en rondas, lo que hace que la estrategia de selección de cartas sea clave.</p>
      <ul>
        <li><strong>Restricciones:</strong> Los jugadores deben seleccionar cartas de los packs proporcionados en el
          evento y construir su mazo durante el Draft.</li>
        <li><strong>Duración:</strong> El formato Draft es ideal para jugadores que disfrutan de la toma de decisiones y
          la flexibilidad en la construcción del mazo.</li>
      </ul>
    </section>

